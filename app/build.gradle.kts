// Read NEWS_API_KEY from local.properties
fun getNewsApiKey(): String {
    val localPropertiesFile = rootProject.file("local.properties")
    if (localPropertiesFile.exists()) {
        localPropertiesFile.readLines().forEach { line ->
            if (line.startsWith("NEWS_API_KEY=")) {
                return line.substringAfter("NEWS_API_KEY=").trim()
            }
        }
    }
    return ""
}

